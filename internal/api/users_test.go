func TestCreateUser_NewUser_AutoGeneratedToken(t *testing.T) {
	store := newFakeStore()
	store.orgTokens["org-secret"] = &auth.OrgToken{Token: "org-secret", OrgID: "my-org", CreatedAt: time.Now()}
	mux := newMux(store)

	w := do(mux, "POST", "/api/v1/users", "org-secret", bodyJSON(t, map[string]string{
		"user_id":     "alice",
		"description": "Alice",
	}))
	if w.Code != http.StatusOK {
		t.Fatalf("expected 200, got %d: %s", w.Code, w.Body.String())
	}

	var user auth.User
	if err := json.NewDecoder(w.Body).Decode(&user); err != nil {
		t.Fatal(err)
	}
	if user.UserID != "alice" {
		t.Errorf("user_id = %q, want alice", user.UserID)
	}
	if user.Token == "" {
		t.Error("token should be auto-generated, got empty string")
	}
	if user.OrgID != "my-org" {
		t.Errorf("org_id = %q, want my-org", user.OrgID)
	}
}

