func TestCreateUser_NoOrgToken_Returns401(t *testing.T) {
	store := newFakeStore()
	mux := newMux(store)

	w := do(mux, "POST", "/api/v1/users", "", bodyJSON(t, map[string]string{"user_id": "alice"}))
	if w.Code != http.StatusUnauthorized {
		t.Fatalf("expected 401, got %d: %s", w.Code, w.Body.String())
	}
}

