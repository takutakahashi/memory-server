func TestCreateUser_UpdateExisting_Returns200(t *testing.T) {
	store := newFakeStore()
	store.orgTokens["org-secret"] = &auth.OrgToken{Token: "org-secret", OrgID: "my-org", CreatedAt: time.Now()}
	store.users["alice"] = &auth.User{
		UserID:    "alice",
		Token:     "old-token",
		OrgID:     "my-org",
		CreatedAt: time.Now().Add(-24 * time.Hour),
	}
	mux := newMux(store)

	w := do(mux, "POST", "/api/v1/users", "org-secret", bodyJSON(t, map[string]any{
		"user_id": "alice",
		"token":   "new-token",
	}))
	if w.Code != http.StatusOK {
		t.Fatalf("expected 200, got %d: %s", w.Code, w.Body.String())
	}

	var user auth.User
	if err := json.NewDecoder(w.Body).Decode(&user); err != nil {
		t.Fatalf("decode response: %v", err)
	}
	if user.Token != "new-token" {
		t.Errorf("token = %q, want new-token", user.Token)
	}
	// created_at should be preserved
	if user.CreatedAt.IsZero() {
		t.Error("created_at should be preserved")
	}
	if time.Since(user.CreatedAt) < time.Hour {
		t.Error("created_at should be the original old value, not now")
	}
}

