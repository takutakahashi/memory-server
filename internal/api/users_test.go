// fakeUserStore is an in-memory implementation of auth.UserStorer for tests.
type fakeUserStore struct {
	users     map[string]*auth.User
	orgTokens map[string]*auth.OrgToken
}

func newFakeStore() *fakeUserStore {
	return &fakeUserStore{
		users:     make(map[string]*auth.User),
		orgTokens: make(map[string]*auth.OrgToken),
	}
}

func (f *fakeUserStore) GetOrgToken(_ context.Context, token string) (*auth.OrgToken, error) {
	t, ok := f.orgTokens[token]
	if !ok {
		return nil, fmt.Errorf("%w: org token", auth.ErrNotFound)
	}
	return t, nil
}

func (f *fakeUserStore) PutUser(_ context.Context, u *auth.User) error {
	cp := *u
	f.users[u.UserID] = &cp
	return nil
}

func (f *fakeUserStore) GetUser(_ context.Context, userID string) (*auth.User, error) {
	u, ok := f.users[userID]
	if !ok {
		return nil, fmt.Errorf("%w: user %s", auth.ErrNotFound, userID)
	}
	return u, nil
}

func (f *fakeUserStore) GetUserByToken(_ context.Context, token string) (*auth.User, error) {
	for _, u := range f.users {
		if u.Token == token {
			cp := *u
			return &cp, nil
		}
	}
	return nil, fmt.Errorf("%w: user token", auth.ErrNotFound)
}

// helpers

func newMux(store *fakeUserStore) *http.ServeMux {
	mux := http.NewServeMux()
	userSrv := api.NewUserServer(store)
	userSrv.RegisterUserRoutes(mux, auth.OrgTokenAuth(store))
	return mux
}

func bodyJSON(t *testing.T, v any) *bytes.Buffer {
	t.Helper()
	b, err := json.Marshal(v)
	if err != nil {
		t.Fatal(err)
	}
	return bytes.NewBuffer(b)
}

func do(mux *http.ServeMux, method, path, orgToken string, body *bytes.Buffer) *httptest.ResponseRecorder {
	if body == nil {
		body = bytes.NewBuffer(nil)
	}
	req := httptest.NewRequest(method, path, body)
	if orgToken != "" {
		req.Header.Set("Authorization", "Bearer "+orgToken)
	}
	req.Header.Set("Content-Type", "application/json")
	w := httptest.NewRecorder()
	mux.ServeHTTP(w, req)
	return w
}

